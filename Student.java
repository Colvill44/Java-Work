import java.util.Scanner;

public class Student{
  
  private String name;
  private int id;
  private boolean reg;
  
  public String getName(){
    return name;
  }
  public void setName(String name){
    this.name = name;
  }
  public int getId(){
    return id;
  }
  public void setId(int id){
    this.id = id;
  }
  public boolean getReg(){
    return reg;
  }
  public void setReg(boolean reg){
    this.reg = reg;
  }
  
}
  